#hacer un codigo que se recorra del 0 a la cantidad que se coloque en la terminal e imprimir si la posision es par o impar

numero = int(input("digite el numero"))
for i in range (numero+1):
   
    if i % 2 == 0:
        print(f"El número {i} es PAR.")
    else:
        print(f"El número {i} es IMPAR.")

